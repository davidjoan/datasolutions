<h2>Symfony</h2>

<p>Symfony es un framework para el desarrollo profesional de
aplicaciones avanzadas de PHP. La inteligencia de su código permite un
desarrollo rápido y robusto sin renunciar a un máximo nivel de control.
Symfony está indicado para proyectos a medida de gran envergadura que
necesiten una evolución constante y precisa.
El éxito de Symfony ha quedado claramente demostrado y a día de
hoy empresas como Yahoo! confían en esta tecnología para el desarrollo
de aplicaciones con más de 200 millones de usuarios.
<p>Symfony  está creado desde el principio para PHP 5 para aprovechar
todo el potencial de PHP. Su código fuente se renueva constantemente y
se garantiza el soporte a largo término para sus  versiones estables. </p>

<figure><?php echo image_tag('frontend/symfony-map.png');?></figure>

<p>A medida que su código fuente evoluciona y es desarrollado se
analiza y prueba haciéndose más de 9000 test unitarios y funcionales
garantizando así su eficiencia y seguridad.</p>
<p>Symfony  incluye la protección contra ataques con Scripts
maliciosos del tipo XSS (Cross-site scripting) y peticiones CSRF
(Cross-site request forgery). </p>
<p>La gran documentación y soporte que ofrece Symfony y su constante
actualización de en multitud de idiomas (más de 40) hacen de Symfony  un
valor seguro y una tecnología de futuro muy a tener en cuenta.</p>

<h2>Doctrine</h2>
<div class="img-box" style="text-align: justify">
<p>Según la documentación oficial, Doctrine es un ORM para PHP 5.2.3 y posterior. Además de todas las ventajas que conlleva un ORM, uno de sus puntos fuertes es su lenguaje DQL (Doctrine Query Language) inspirado en el HQL de Hibernate.</p>
<figure><?php echo image_tag('frontend/orm.png');?></figure>
<p>Cuando trabajamos con Doctrine, necesitamos informar a su motor interno de cual es el modelo de nuestra aplicación, para ello podemos hacer ingeniería inversa de nuestra base de datos existente, o si empezamos la aplicación desde 0, crear el modelo en la sintaxis específica que nos propone Doctrine y luego generar toda la base de datos.</p>
</div>
